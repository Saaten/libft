char	*ft_strrchr(const char *s, int c)
{
	char	*i;

	i = NULL;
	while (*s)
	{
		if (*s == (char) c)
			i = (char *) s;
		s++;
	}
	if ((char)c == '\0')
		return ((char *) s);
	return (i);
}
